import unittest

import numpy as np

from measure import Operation, operation, confusions, alphabet
import pandas as pd
from io import StringIO


class TestMeasure(unittest.TestCase):

    def test_replace(self):
        res = operation("tpe", "the")
        self.assertEqual((Operation.REPLACE, "p", "h"), res)

        res = operation("t", "e")
        self.assertEqual((Operation.REPLACE, "t", "e"), res)

    def test_transpose(self):
        res = operation("te", "et")
        self.assertEqual((Operation.TRANSPOSE, "t", "e"), res)
        res = operation("teh", "the")
        self.assertEqual((Operation.TRANSPOSE, "e", "h"), res)

    def test_insert(self):
        res = operation("", "t")
        self.assertEqual((Operation.INSERT, "_", "t"), res)
        res = operation("t", "ht")
        self.assertEqual((Operation.INSERT, "_", "h"), res)
        res = operation("en", "etn")
        self.assertEqual((Operation.INSERT, "e", "t"), res)
        res = operation("th", "the")
        self.assertEqual((Operation.INSERT, "h", "e"), res)

    def test_delete(self):
        res = operation("th", "t")
        self.assertEqual((Operation.DELETE, "t", "h"), res)
        res = operation("th", "h")
        self.assertEqual((Operation.DELETE, "_", "t"), res)
        res = operation("etn", "en")
        self.assertEqual((Operation.DELETE, "e", "t"), res)
        res = operation("the", "th")
        self.assertEqual((Operation.DELETE, "h", "e"), res)


